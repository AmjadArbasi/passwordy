/// This class handles all the names of the [routes] that being use
///
class AppRoutes {
  /// The [root]
  static const String root = "/";

  static const String splash = "${root}spash";

  /// [Skeleton] of the main pages
  static const String skeleton = "${root}skeleton";

  /// The [home]
  static const String home = "${root}home";

  /// The [vault]
  static const String vault = "${root}vault";

  /// The [vault sub pages]
  /// The [save new or edit secret]
  static const String saveCatchword = "${root}saveCatchword";

  static const String viewCatchword = "${root}viewCatchword";

  /// The `save category` subpage
  static const String saveCategory = "${root}saveCategory";

  /// The [profile]
  static const String profile = "${root}profile";

  /// The [profile sub pages]
  /// The [general info about the registerd user]
  static const String general = "${root}general";

  /// The [some information about the app]
  static const String about = "${root}about";

  /// The [Shows the activites log of all the activities]
  static const String activitiesLog = "${root}activitiesLog";

  /// The [Pages for Login (sign in and sign up)]
  static const String signIn = "${root}signIn";
  static const String signUp = "${root}signUp";

  /// The [On boarding Page]
  static const String onBoarding = "${root}onBoarding";

  /// The [Translator] page
  static const String translator = "${root}translator";

  static const String welcome = "${root}welcome";

  static const String resetPassword = "${root}resetPassword";
}
